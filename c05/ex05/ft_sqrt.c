int	ft_sqrt(int nb)
{
	long a;

	a = 1;
	while (1)
	{
		a = (a + nb / a) / 2;
		if (nb - 1 < a * a && a * a < nb + 1)
			break ;
		else if (a * a - nb < 1 || nb < 0)
			return (0);
	}
	return (a);
}
